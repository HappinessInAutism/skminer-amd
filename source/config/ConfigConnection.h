#ifndef _CONFIGCONNECTION_H_
#define _CONFIGCONNECTION_H_

#include <string>
#include "../base/IPrototype.h"

class ConfigConnection : public IPrototype
{

private:
	
	std::string m_szURL;
	std::string m_szUser;
	std::string m_szPassword;

public:

	///////////////////////////////////////////////////////////////////////////////
	//Constructor
	///////////////////////////////////////////////////////////////////////////////
	ConfigConnection();

	///////////////////////////////////////////////////////////////////////////////
	//Copy Constructor
	///////////////////////////////////////////////////////////////////////////////
	ConfigConnection(const ConfigConnection& conn);

	///////////////////////////////////////////////////////////////////////////////
	//Assignment Operator
	///////////////////////////////////////////////////////////////////////////////
	ConfigConnection& operator=(const ConfigConnection& conn);

	///////////////////////////////////////////////////////////////////////////////
	//Destructor
	///////////////////////////////////////////////////////////////////////////////
	~ConfigConnection();

	
	ConfigConnection* Clone();
	ConfigConnection* DeepCopy();

	///////////////////////////////////////////////////////////////////////////////
	//Accessors
	///////////////////////////////////////////////////////////////////////////////
	const std::string& GetURL()			const		{	return this->m_szURL;				}
	const std::string& GetUser()		const		{	return this->m_szUser;				}
	const std::string& GetPassword()	const		{	return this->m_szPassword;			}

	///////////////////////////////////////////////////////////////////////////////
	//Mutators
	///////////////////////////////////////////////////////////////////////////////
	void SetURL(const std::string szURL)			{	this->m_szURL = szURL;				} 
	void SetUser(const std::string szUser)			{	this->m_szUser = szUser;			}
	void SetPassword(const std::string szPassword)	{	this->m_szPassword = szPassword;	}

};


#endif //_CONFIGCONNECTION_H_