#ifndef _SECTION_H_
#define _SECTION_H_

#include <string>
#include <vector>

class Section
{
private:

	std::string m_szTitle;
	std::vector<std::string> m_vecLines;

public:

	///////////////////////////////////////////////////////////////////////////////
	//Constructor
	///////////////////////////////////////////////////////////////////////////////
	Section();

	///////////////////////////////////////////////////////////////////////////////
	//Copy Constructor
	///////////////////////////////////////////////////////////////////////////////
	Section(const Section& section);

	///////////////////////////////////////////////////////////////////////////////
	//Assignment Operator
	///////////////////////////////////////////////////////////////////////////////
	Section& operator=(const Section& section);

	///////////////////////////////////////////////////////////////////////////////
	//Destructor
	///////////////////////////////////////////////////////////////////////////////
	~Section();

	///////////////////////////////////////////////////////////////////////////////
	//Accessors
	///////////////////////////////////////////////////////////////////////////////
	const std::string& GetTitle()				const		{	return this->m_szTitle;		}
	const std::vector<std::string>&	GetLines()	const		{	return this->m_vecLines;	}

	///////////////////////////////////////////////////////////////////////////////
	//Mutators
	///////////////////////////////////////////////////////////////////////////////
	void	SetTitle(const std::string& title)				{	this->m_szTitle = title;	}
	void	SetLines(const std::vector<std::string>	lines)	{	this->m_vecLines = lines;	}
};

#endif //_SECTION_H_